function templates = SystemTemplates()
    templates = {
        % 1. 改进的Chen系统
        @(t,X,p) [
            p(1)*(X(2) - X(1)) + X(2)*X(3);
            (p(3) - p(1))*X(1) - X(1)*X(3) + p(4)*X(2);
            X(1)*X(2) - p(2)*X(3)
        ];
        
        % 2. 增强型Lorenz系统
        @(t,X,p) [
            p(1)*(X(2) - X(1)) + p(4)*X(2)*X(3);
            p(3)*X(1) - X(2) - X(1)*X(3);
            X(1)*X(2) - p(2)*X(3) + p(5)*X(1)*X(3)
        ];
        
        % 3. 修改的Rössler系统
        @(t,X,p) [
            -X(2) - X(3) + p(4)*X(1)*X(2);
            X(1) + p(1)*X(2) + p(5)*X(2)*X(3);
            p(2) + X(3)*(X(1) - p(3))
        ];
        
        % 4. 超混沌系统
        @(t,X,p) [
            p(1)*(X(2) - X(1)) + p(4)*X(2)*X(3);
            p(3)*X(1) - X(1)*X(3) + p(5)*X(2);
            X(1)*X(2) - p(2)*X(3) + p(6)*X(1)^2
        ]
    };
end